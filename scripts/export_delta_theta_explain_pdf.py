#!/usr/bin/env python3
"""Export the delta-vs-theta explanation (Hay eli example) to PDF."""
from __future__ import annotations

from pathlib import Path

from fpdf import FPDF

ROOT = Path(__file__).resolve().parents[1]
OUT = ROOT / "docs" / "delta_vs_theta_hay_eli.pdf"
FONT = "/System/Library/Fonts/Supplemental/Arial Unicode.ttf"


class Pdf(FPDF):
    def footer(self) -> None:
        self.set_y(-12)
        self.set_font("AU", "", 8)
        self.set_text_color(120, 120, 120)
        self.cell(0, 8, f"chgk-model · стр. {self.page_no()}", align="C")


def main() -> None:
    pdf = Pdf()
    pdf.set_auto_page_break(auto=True, margin=18)
    pdf.add_font("AU", "", FONT)
    pdf.add_font("AU", "B", FONT)
    pdf.set_margins(18, 18, 18)
    pdf.add_page()
    wrap_w = pdf.epw

    def h1(text: str) -> None:
        pdf.set_font("AU", "B", 16)
        pdf.set_text_color(30, 41, 59)
        pdf.multi_cell(wrap_w, 9, text)
        pdf.ln(2)

    def h2(text: str) -> None:
        pdf.ln(3)
        pdf.set_font("AU", "B", 12)
        pdf.set_text_color(51, 65, 85)
        pdf.multi_cell(wrap_w, 7, text)
        pdf.ln(1)

    def body(text: str) -> None:
        pdf.set_font("AU", "", 10)
        pdf.set_text_color(15, 23, 42)
        pdf.multi_cell(wrap_w, 5.5, text)
        pdf.ln(1)

    def bullet(text: str) -> None:
        pdf.set_font("AU", "", 10)
        pdf.set_text_color(15, 23, 42)
        pdf.multi_cell(wrap_w, 5.5, f"  •  {text}")

    h1("Почему Δ турнира и изменение θ — разные вещи")
    body(
        "Пример: синхрон Hay eli (турнир 11766), команда «Конь Текст». "
        "Команда сыграла в +0,5 к прогнозу (17 взятых при ожидании 16,48), "
        "но сила Максима Сидорова выросла на ~0,014, а у Сергея Задворьева "
        "упала на ~0,059. Ниже — как устроена модель на chgk.quest и почему "
        "так не противоречие."
    )

    h2("1. Что означает Δ = +0,5")
    body(
        "Δ в таблице турнира — это суммарный сюрприз по пакету для команды:"
    )
    pdf.set_font("AU", "", 10)
    pdf.multi_cell(wrap_w, 6, "       Δ = sum(взяли - p_ожид)  ~  +0,52")
    body(
        "Ожидание считается до турнира: состав, θ каждого игрока на тот момент, "
        "сложность вопросов (b), поправки на размер команды и позицию в туре, "
        "калибровка формата (lapse/recal). Это не «насколько выросла сила», "
        "а «насколько результат команды был лучше прогноза»."
    )

    h2("2. Как после турнира меняется θ")
    body("Модель обрабатывает турнир по каждому вопросу (36 штук на Hay eli):")
    bullet("взяли вопрос → θ всех в составе слегка увеличивается;")
    bullet("не взяли → слегка уменьшается.")
    body(
        "В формуле noisy-OR вклад игрока k пропорционален λk = exp(−b + θk): "
        "кто в прогнозе «тащит» вопрос сильнее, тот сильнее и награждается "
        "за взятие, и сильнее штрафуется за промах на этом вопросе."
    )
    body(
        "После всех вопросов срабатывает teammate-shrinkage: θ состава чуть "
        "подтягиваются к среднему θ этой команды на турнире (η = 0,005)."
    )
    body(
        "На сайте в профиле игрока колонка «θ после» — снимок силы после "
        "обработки турнира. Разница с предыдущей строкой — суммарный эффект "
        "именно этого турнира (если смотреть соседние игры в хронологии)."
    )

    h2("3. Конкретно «Конь Текст» на Hay eli")
    body("θ до / после турнира (данные сайта):")

    pdf.set_font("AU", "B", 9)
    col_w = (48, 30, 30, 24)
    headers = ("Игрок", "theta до", "theta после", "D theta")
    x0 = pdf.l_margin
    for i, h in enumerate(headers):
        pdf.cell(col_w[i], 7, h, border=1, align="C")
    pdf.ln()
    pdf.set_font("AU", "", 9)
    rows = [
        ("Сидоров М.", "+0,224", "+0,238", "+0,013"),
        ("Задворьев С.", "-1,075", "-1,133", "-0,059"),
        ("остальные 4", "~ -1,4..-1,6", "~ то же", "~ 0,000"),
    ]
    for row in rows:
        pdf.set_x(x0)
        for i, cell in enumerate(row):
            pdf.cell(col_w[i], 7, cell, border=1, align="C" if i else "L")
        pdf.ln()

    pdf.set_x(x0)
    pdf.ln(2)
    body("Команда в целом чуть переиграла прогноз, но перераспределение θ — не «всем в плюс».")

    h2("4. Почему у Задворьева минус при +0,5 у команды")

    body("Разный вес в прогнозе. Средний θ состава ≈ −1,17. Сидоров на +0,224 — "
         "примерно на 1,4 выше среднего; Задворьев на −1,075 — выше «четвёрки» "
         "(−1,4…−1,6), но вклад в noisy-OR у Сидорова на порядок больше. "
         "Большая часть ожидаемых 16,48 взятий в модели приписана ему. На взятых "
         "вопросах основной плюс к θ — у Сидорова; на промахах, где вероятность "
         "уже была заметной, основной минус — у тех, кого модель считала опорой "
         "в составе (здесь — Задворьев относительно остальных, кроме Сидорова).")

    body("Разная скорость обучения: ηk = η0 / √(0,25 + gamesk). У Задворьева "
         "~26 турниров в базе → шаги крупнее; у Сидорова ~125 → шаги мельче. "
         "При схожих градиентах итоговый сдвиг по модулю у менее опытного "
         "игрока может быть больше.")

    body("Сжатие к среднему состава: Сидоров сильно выше среднего ростера → "
         "shrinkage давит вниз, но SGD-плюс перекрывает (+0,013). Задворьев "
         "чуть выше «четвёрки» → тоже вниз; вместе с SGD выходит −0,059. "
         "Четверо с θ ≈ −1,5 почти в центре массы — почти нулевое изменение.")

    h2("5. Что Δ не означает")
    bullet("Δ не делится между игроками пропорционально «заслуге» на турнире.")
    bullet("+0,5 у команды не означает «у всех θ должен вырасти».")
    bullet("Сравнивать нужно θ до и θ после по строкам одного игрока, "
           "а не «мой Δ турнира = мой Δθ».")

    h2("Краткий вывод")
    body(
        "Команда слегка переиграла ожидание, но модель видит состав как "
        "«сильный Сидоров + более слабые остальные, Задворьев чуть выделяется "
        "среди четверых». Плюс на уровне команды не обязан дать плюс каждому: "
        "это следствие покомандного разбора 36 вопросов, разного веса в noisy-OR "
        "и разного числа игр (скорость обучения), а не ошибки Δ."
    )

    pdf.ln(2)
    pdf.set_font("AU", "", 9)
    pdf.set_text_color(79, 70, 229)
    pdf.multi_cell(
        wrap_w,
        5,
        "Ссылки: chgk.quest/tournament/11766 · chgk.quest/player/232138",
    )

    OUT.parent.mkdir(parents=True, exist_ok=True)
    pdf.output(str(OUT))
    print(OUT)


if __name__ == "__main__":
    main()
